                <li>姓名不是纯文本形式（改为纯文本没有数字或特殊字符）</li>
                <li>地址不是纯文本形式（改为纯文本没有数字或特殊字符）</li>
                <li>年龄不是纯数字形式（改为纯数字没有文本或特殊字符）</li>
                <li>性别输入格式不对（正确格式：男/女）</li>
                <li>体重不是纯数字形式（改为纯数字没有文本或特殊字符）</li>
                <li>身高不是纯数字形式（改为纯数字没有文本或特殊字符）</li>
                <li>血型输入格式不对（正确格式：A/B/AB/O/ABO）</li>
              </ul>

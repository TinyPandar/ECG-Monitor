              <p>5.1.1 用户名/密码输入错误</p>
              <p>若用户密码输入完毕后上方红色弹框“用户名或密码输入错误！”，代表用户所输入的用户名或密码有误，再次输入正确用户名或密码后即可顺利登录。</p>
              
              <p>5.1.2 用户注册密码强度低</p>
              <p>若用户注册密码输入完毕后上方红色弹框“密码强度太低！”，代表用户所输入的密码长度不够六位或密码没有同时包含数字和字母，重新设置大于六位且同时包含数字和字母的密码后即可顺利注册。</p>

              
              <p>5.1.3 用户注册密码输入不同</p>
              <p>若用户注册密码输入完毕后上方红色弹框“两次密码输入不同，请检查后重新注册！”，代表用户两次所输入的密码不相同，重新输入两次密码，确保相同后即可顺利注册。</p>
              
              <h3>5.2 个人信息录入/修改出错及恢复</h3>
              <p>5.2 个人信息无法保存</p>
              <p>填写完个人信息点击保存没有反应，可能情况如下：</p>
              <ul>
